-- Pane remapping
vim.cmd [[nmap <silent> <c-h> <cmd>wincmd h<cr>]]
vim.cmd [[nmap <silent> <c-j> <cmd>wincmd j<cr>]]
vim.cmd [[nmap <silent> <c-k> <cmd>wincmd k<cr>]]
vim.cmd [[nmap <silent> <c-l> <cmd>wincmd l<cr>]]

-- Tab Mapping
vim.cmd [[nmap <leader>w <cmd>tabnew<cr>]]
vim.cmd [[nmap <leader>n <cmd>tabnext<cr>]]
vim.cmd [[nmap <leader>p <cmd>tabprevious<cr>]]
vim.cmd [[nmap <leader>c <cmd>tabc<cr>]]
